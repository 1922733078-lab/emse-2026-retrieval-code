_LETTERS = "ABCDEFG"


def _token(letter: str | None = None, digit: int | None = None) -> str:
    letter = letter or random.choice(_LETTERS)
    digit = digit if digit is not None else random.randint(0, 9)
    return f"{letter}{digit}"


def _series(
    length: int | None = None,
    start_ts: int | None = None,
    step: int | None = None,
    low_value: bool = False,
) -> tuple[str, dict[str, Any]]:
    """Return a valid Nimbla series string and its parsed record."""
    length = length if length is not None else random.randint(3, 6)
    start_ts = start_ts if start_ts is not None else random.randint(1, 50)
    step = step if step is not None else random.randint(1, 5)
    timestamps = [start_ts + i * step for i in range(length)]
    if low_value:
        letters = "AB"
        tokens = [f"{random.choice(letters)}{random.randint(0, 9)}" for _ in range(length)]
    else:
        tokens = [_token() for _ in range(length)]
    values = [nimbla.nimbla_value(t) for t in tokens]
    series_str = "NIM|" + ",".join(f"{ts}:{tok}" for ts, tok in zip(timestamps, tokens))
    return series_str, {"timestamps": timestamps, "values": values}


def _low_value_series(length: int = 3) -> tuple[str, dict[str, Any]]:
    """Return a series whose values are small enough to survive scaling."""
    return _series(length=length, low_value=True)


def _two_series() -> tuple[tuple[str, dict[str, Any]], tuple[str, dict[str, Any]]]:
    s1, r1 = _series(length=random.randint(2, 4), start_ts=1, step=2)
    s2, r2 = _series(length=random.randint(2, 4), start_ts=2, step=2)
    return (s1, r1), (s2, r2)


def _multi_series(n: int = 3) -> tuple[list[str], list[dict[str, Any]]]:
    strings, recs = [], []
    for _ in range(n):
        s, r = _series(length=random.randint(2, 4))
        strings.append(s)
        recs.append(r)
    return strings, recs


def _fmt_assert(expr: str, expected: Any) -> str:
    """Format an assert string comparing expr to expected."""
    if isinstance(expected, bool):
        return f"assert {expr} is {expected}"
    if expected is None:
        return f"assert {expr} is None"
    if isinstance(expected, str):
        return f"assert {expr} == {expected!r}"
    if isinstance(expected, (int,)):
        return f"assert {expr} == {expected}"
    if isinstance(expected, float):
        return f"assert abs({expr} - {expected}) < 1e-9"
    if isinstance(expected, list):
        if not expected:
            return f"assert {expr} == []"
        if all(isinstance(x, (int, str)) for x in expected):
            return f"assert {expr} == {expected}"
        return (
            f"assert len({expr}) == {len(expected)} and "
            f"all(abs(a - b) < 1e-9 for a, b in zip({expr}, {expected}))"
        )
    if isinstance(expected, dict):
        if not expected:
            return f"assert {expr} == {{}}"
        parts = []
        for k, v in expected.items():
            if isinstance(v, float):
                parts.append(f"abs({expr}[{k!r}] - {v}) < 1e-9")
            elif isinstance(v, int):
                parts.append(f"{expr}[{k!r}] == {v}")
            elif isinstance(v, bool):
                parts.append(f"{expr}[{k!r}] is {v}")
            elif isinstance(v, list):
                parts.append(f"len({expr}[{k!r}]) == {len(v)}")
            else:
                parts.append(f"{expr}[{k!r}] == {v!r}")
        return "assert " + " and ".join(parts)
    return f"assert {expr} == {expected!r}"


def _solve(code: str, *args: Any) -> Any:
    """Execute a reference solution and call solve with the given args."""
    namespace: dict[str, Any] = dict(nimbla.__dict__)
    exec(code, namespace)
    return namespace["solve"](*args)


