tasks: list[dict[str, Any]] = []


def add_task(
    task_id: str,
    level: str,
    category: str,
    signature: str,
    prompt: str,
    visible_tests: list[str],
    hidden_tests: list[str],
    gold_snippets: list[str],
    distractor_snippets: list[str],
    reasoning_steps: int,
    reference_code: str,
) -> None:
    task = {
        "task_id": task_id,
        "level": level,
        "category": category,
        "signature": signature,
        "prompt": prompt,
        "visible_tests": visible_tests,
        "hidden_tests": hidden_tests,
        "gold_snippets": gold_snippets,
        "distractor_snippets": distractor_snippets,
        "reasoning_steps": reasoning_steps,
        "reference_solution_path": f"solutions/nimbla/{task_id}.py",
    }
    tasks.append(task)
    (SOLUTIONS_DIR / f"{task_id}.py").write_text(reference_code, encoding="utf-8")


def make_task(
    task_id: str,
    level: str,
    category: str,
    signature: str,
    prompt: str,
    reference_code: str,
    visible_inputs: list[tuple[Any, ...]],
    hidden_inputs: list[tuple[Any, ...]],
    gold_snippets: list[str],
    distractor_snippets: list[str],
    reasoning_steps: int,
) -> None:
    """Build a task by executing the reference solution on all inputs."""
    visible_tests = []
    for args in visible_inputs:
        result = _solve(reference_code, *args)
        visible_tests.append(_fmt_assert(_call_expr("solve", args), result))

    hidden_tests = []
    for args in hidden_inputs:
        result = _solve(reference_code, *args)
        hidden_tests.append(_fmt_assert(_call_expr("solve", args), result))

    add_task(
        task_id=task_id,
        level=level,
        category=category,
        signature=signature,
        prompt=prompt,
        visible_tests=visible_tests,
        hidden_tests=hidden_tests,
        gold_snippets=gold_snippets,
        distractor_snippets=distractor_snippets,
        reasoning_steps=reasoning_steps,
        reference_code=reference_code,
    )


def _call_expr(name: str, args: tuple[Any, ...]) -> str:
    parts = []
    for a in args:
        if isinstance(a, str):
            parts.append(repr(a))
        else:
            parts.append(repr(a))
    return f"{name}({', '.join(parts)})"


