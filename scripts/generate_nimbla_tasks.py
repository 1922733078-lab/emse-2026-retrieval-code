def main() -> None:
    l1_tasks()
    l2_tasks()
    l3_tasks()

    l1 = [t for t in tasks if t["level"] == "L1"]
    l2 = [t for t in tasks if t["level"] == "L2"]
    l3 = [t for t in tasks if t["level"] == "L3"]

    for level_name, level_tasks in [("l1", l1), ("l2", l2), ("l3", l3)]:
        path = TASKS_DIR / f"nimbla_{level_name}.jsonl"
        with path.open("w", encoding="utf-8") as f:
            for task in level_tasks:
                f.write(json.dumps(task, ensure_ascii=False) + "\n")

    combined_path = TASKS_DIR / "nimbla_all.jsonl"
    with combined_path.open("w", encoding="utf-8") as f:
        for task in tasks:
            f.write(json.dumps(task, ensure_ascii=False) + "\n")

    print(f"Generated {len(l1)} L1 + {len(l2)} L2 + {len(l3)} L3 = {len(tasks)} tasks.")
    print(f"Tasks written to {TASKS_DIR}")
    print(f"Reference solutions written to {SOLUTIONS_DIR}")


if __name__ == "__main__":
    main()
